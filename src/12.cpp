// This is a simple program that demonstrates how to add and subtract numbers using C++.

#include <iostream>
using namespace std;

int main() {
    int num1 = 5;
    int num2 = 3;

    // Addition
    cout << "The sum of " << num1 << " and " << num2 << " is: " << (num1 + num2) << endl;

    // Subtraction
    cout << "The difference between " << num1 << " and " << num2 << " is: " << (num1 - num2) << endl;
}
